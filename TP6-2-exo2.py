liste = [i for i in range(0,11)]
print(liste)
print(liste[0]) # extraction de ...premier elt
print(liste[:]) # extraction de ...toute la liste
print(liste[:3]) # extraction de ...du début jusqu'au 3 elt 
print(liste[3:]) # extraction de ...du 4ème elt jusqu'à la fin
print(liste[3:7]) # extraction de ...du 4ème elt jusqu'au 7 ème elt 
print(liste[-1]) # extraction de ...dernier elt
print(liste[-4:]) # extraction de ...du 4ème avant dernier elt jusquà la fin